#include "3-calc.h"
/**
 * op_add - sum to variables
 *
 * @a: int first param
 * @b: int second param
 *
 * Return: int sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts a from b
 *
 * @a: int first param
 * @b: int second param
 *
 * Return: int subraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplicates a and b
 *
 * @a: int first param
 * @b: int second param
 *
 * Return: int multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 *
 * @a: int first param
 * @b: int second param
 *
 * Return: int division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - remainder of a division
 *
 * @a: int first param
 * @b: int second param
 *
 * Return: int remaindes of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
